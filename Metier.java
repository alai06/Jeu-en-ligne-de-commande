public class Metier{
    public String nom;
    public int pvmax;
    public int pmmax;
    public int degat;
    public int protec;
    public Metier(String nom, int pvmax, int pmmax, int degat, int protec){
        this.nom = nom;
        this.pvmax = pvmax;
        this.pmmax = pmmax;
        this.degat = degat;
        this.protec = protec;
    }
}
